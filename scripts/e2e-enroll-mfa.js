const SECRETS_FILE = path.join(__dirname, '.e2e-mfa-secrets.env');

/** Read the existing gitignored secrets file into { ENV_NAME: value }. */
function readSecretsFile() {
  const out = {};
  if (!fs.existsSync(SECRETS_FILE)) return out;
  for (const line of fs.readFileSync(SECRETS_FILE, 'utf8').split('\n')) {
    const m = line.match(/^\s*(?:export\s+)?([A-Z0-9_]+)=(.*)$/);
    if (m) out[m[1]] = m[2].replace(/^['"]|['"]$/g, '');
  }
  return out;
}

/** Merge-write the secrets file (chmod 600). Values are NEVER printed. */
function writeSecretsFile(secrets) {
  const header =
    '# E2E TOTP secrets — GITIGNORED, never commit. Generated by scripts/e2e-enroll-mfa.js.\n' +
    '# Load before running persona-safety.spec.ts:  set -a; source scripts/.e2e-mfa-secrets.env; set +a\n';
  const body = Object.entries(secrets)
    .map(([k, v]) => `${k}='${v}'`)
    .join('\n');
  fs.writeFileSync(SECRETS_FILE, header + body + '\n', { mode: 0o600 });
  try {
    fs.chmodSync(SECRETS_FILE, 0o600);
  } catch {
    /* best-effort */
  }
}

(async () => {
  const app = initializeApp(firebaseConfig);
  const auth = getAuth(app);

  const secrets = readSecretsFile();
  const results = [];

  for (const f of FIDUCIARIES) {
    let cred;
    try {
      cred = await signInWithEmailAndPassword(auth, f.email, PASSWORD);
    } catch (e) {
      // IDEMPOTENCY: once a TOTP factor exists, sign-in itself throws
      // auth/multi-factor-auth-required BEFORE returning a user — that is the
      // definitive "already enrolled" signal. Skip without re-minting (a re-enroll
      // would invalidate the captured secret the login helper relies on).
      if ((e.code || '') === 'auth/multi-factor-auth-required') {
        const haveStored = Boolean(secrets[f.envVar]);
        console.log(
          `  ${f.key.padEnd(8)} already enrolled — skip. ` +
            (haveStored
              ? `(${f.envVar} present in secrets file)`
              : `WARNING: no stored ${f.envVar}; unenroll this factor + re-run to capture a fresh secret.`),
        );
        results.push({ key: f.key, envVar: f.envVar, status: haveStored ? 'enrolled' : 'enrolled-no-secret' });
        continue;
      }
      console.error(`  ${f.key.padEnd(8)} SIGN-IN FAILED for ${f.email}: ${e.code || e.message}`);
      process.exitCode = 1;
      continue;
    }
    const user = cred.user;

    // Belt-and-suspenders: if an account somehow returns a user yet already has a
    // TOTP factor (no MFA challenge configured), skip rather than double-enroll.
    const already = multiFactor(user).enrolledFactors.some(
      (factor) => factor.factorId === TotpMultiFactorGenerator.FACTOR_ID,
    );
    if (already) {
      const haveStored = Boolean(secrets[f.envVar]);
      console.log(
        `  ${f.key.padEnd(8)} already enrolled — skip. ` +
          (haveStored
            ? `(${f.envVar} present in secrets file)`
            : `WARNING: no stored ${f.envVar}; unenroll + re-run to capture a fresh secret.`),
      );
      results.push({ key: f.key, envVar: f.envVar, status: haveStored ? 'enrolled' : 'enrolled-no-secret' });
      continue;
    }

    try {
      const session = await multiFactor(user).getSession();
      const totpSecret = await TotpMultiFactorGenerator.generateSecret(session);
      const base32 = totpSecret.secretKey; // base32 — what otplib consumes

      const code = authenticator.generate(base32);
      const assertion = TotpMultiFactorGenerator.assertionForEnrollment(totpSecret, code);
      await multiFactor(user).enroll(assertion, 'Authenticator App');

      secrets[f.envVar] = base32; // captured to the gitignored file only
      results.push({ key: f.key, envVar: f.envVar, status: 'enrolled' });
      console.log(`  ${f.key.padEnd(8)} ENROLLED — secret captured to ${f.envVar}`);
    } catch (e) {
      console.error(`  ${f.key.padEnd(8)} ENROLLMENT FAILED for ${f.email}: ${e.code || e.message}`);
      if (String(e.code).includes('totp') || /mfa|multi-factor|operation-not-allowed/i.test(String(e.code || e.message))) {
        console.error('    -> Ensure TOTP MFA is ENABLED for finalwishes-prod in the Firebase Console.');
      }
      process.exitCode = 1;
    }
  }

  writeSecretsFile(secrets);

  console.log('\nSecrets written (values redacted) to: scripts/.e2e-mfa-secrets.env  [gitignored, chmod 600]');
  console.log('Set these env var NAMES before running the persona specs:');
  for (const f of FIDUCIARIES) console.log(`  - ${f.envVar}`);
  console.log('\nLoad them with:  set -a; source scripts/.e2e-mfa-secrets.env; set +a');

  // Firebase keeps a live connection open; force exit.
  process.exit(process.exitCode || 0);
})().catch((e) => {
  console.error('MFA enrollment script failed:', e && e.message ? e.message : e);
  process.exit(1);
});
