/**
 * Enroll TOTP MFA for the E2E fiduciary persona accounts (heir + executor) so the
 * MFA-gated persona-safety specs can actually run against LIVE prod.
 *
 * WHY this exists
 * ---------------
 * IdentityGate gates fiduciaries (heir/executor) on getMFAStatus(user).enrolled,
 * which reads multiFactor(user).enrolledFactors from the Firebase CLIENT SDK. The
 * Admin SDK (firebase-admin) can only seed PHONE second factors — there is NO
 * Admin API to enroll a TOTP factor. So scripts/e2e-provision-personas.js seeds the
 * attestation half of the gate but cannot seed the MFA half. THIS script closes that
 * gap by performing a real client-SDK TOTP enrollment, exactly like a real user:
 *
 *   signInWithEmailAndPassword
 *     -> multiFactor(user).getSession()
 *     -> TotpMultiFactorGenerator.generateSecret(session)   (gives base32 secretKey)
 *     -> compute current 6-digit code from that secret via otplib (test-only dep)
 *     -> multiFactor(user).enroll(assertionForEnrollment(secret, code))
 *
 * It mirrors web/src/lib/mfa.ts (startTotpEnrollment / finalizeTotpEnrollment) but
 * is standalone test infra — it does NOT import or modify any production code.
 *
 * The base32 secret for each account is the thing the login helper later needs (to
 * compute a live code at the MFA challenge). We write the secrets to a GITIGNORED
 * local file (scripts/.e2e-mfa-secrets.env) and ONLY print the env-var NAMES — the
 * secret values are never echoed to stdout or committed.
 *
 * IDEMPOTENT: if the account already has an enrolled TOTP factor we skip it and
 * leave any previously-captured secret in the env file untouched. (A re-enroll would
 * mint a NEW secret and invalidate the stored one, so we never re-enroll silently.)
 *
 * RUN
 * ---
 *   E2E_PERSONA_PASSWORD='<pw>' node scripts/e2e-enroll-mfa.js
 *
 * Then load the secrets into your shell before running the specs:
 *   set -a; source scripts/.e2e-mfa-secrets.env; set +a
 *
 * REQUIREMENTS
 *   - Firebase Identity Platform "TOTP MFA" must be ENABLED for finalwishes-prod
 *     (Console -> Authentication -> Sign-in method -> Multi-factor -> Authenticator app).
 *   - The persona accounts must already exist (run e2e-provision-personas.js first).
 */

